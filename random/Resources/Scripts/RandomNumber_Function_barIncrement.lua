function RandomNumber.barIncrement ( nBar )
--------------------------------------------------------------------------------
	
	local hBar = application.getCurrentUserSceneTaggedObject ( "bar" .. nBar )
    if(hBar) then
        local x, y, z = object.getScale ( hBar )
        object.setScale ( hBar, x, y + 0.1, z )
    end
	
--------------------------------------------------------------------------------
end
