function UserAI.onKeyboardKeyDown ( kKeyCode )
--------------------------------------------------------------------------------
	
    if(kKeyCode == input.kKeyLeft )
    then
        this.bLeft ( true )
    elseif(kKeyCode == input.kKeyRight )
    then
        this.bRight ( true )
    elseif(kKeyCode == input.kKeySpace )
    then
        this.bFire ( true )
    end
	
--------------------------------------------------------------------------------
end
