function UserAI.onKeyboardKeyUp ( kKeyCode )
--------------------------------------------------------------------------------
	
	if(kKeyCode == input.kKeyLeft )
    then
        this.bLeft ( false )
    elseif(kKeyCode == input.kKeyRight )
    then
        this.bRight ( false )
    elseif(kKeyCode == input.kKeySpace )
    then
        this.bFire ( false )
    end
	
--------------------------------------------------------------------------------
end
