--------------------------------------------------------------------------------
function InputAI.getKey ( nPlayer, sKey )
--------------------------------------------------------------------------------
	
	local ht
    
    if(nPlayer == 0)
    then
        ht = this.htPlayer1Keys ( )
    elseif(nPlayer == 1)
    then
        ht = this.htPlayer2Keys ( )
    end
    
	local result = hashtable.get ( ht, sKey )
    
    return result
	
--------------------------------------------------------------------------------
end
