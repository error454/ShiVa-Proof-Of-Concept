function FPS_Display_Main.onInit (  )
--------------------------------------------------------------------------------
	
    hud.newTemplateInstance ( this.getUser ( ), "FPS_Display", "FPS_Display" )
    local hHud = hud.getComponent ( this.getUser ( ), "FPS_Display.Display" )
    hud.setComponentVisible ( hHud, false )
    
    this.nDelayTime ( 0.5 ) -- in seconds

--------------------------------------------------------------------------------
end
