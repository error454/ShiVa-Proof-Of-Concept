function HudInterpolation.onKeyboardKeyUp ( kKeyCode )
--------------------------------------------------------------------------------
	
	if(kKeyCode == input.kKeyUp) then
        this.bUp ( false )
    elseif(kKeyCode == input.kKeyDown) then
        this.bDown ( false )
    end
	
--------------------------------------------------------------------------------
end
