function HudInterpolation.onKeyboardKeyDown ( kKeyCode )
--------------------------------------------------------------------------------
	
	if(kKeyCode == input.kKeySpace) then
        this.hudAnimate (  )
    elseif(kKeyCode == input.kKeyUp) then
        this.bUp ( true )
    elseif(kKeyCode == input.kKeyDown) then
        this.bDown ( true )
    end
	
--------------------------------------------------------------------------------
end
