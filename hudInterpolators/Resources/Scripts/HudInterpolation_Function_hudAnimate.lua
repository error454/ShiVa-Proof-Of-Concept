--------------------------------------------------------------------------------
function HudInterpolation.hudAnimate ( )
--------------------------------------------------------------------------------
	
    local hUser = this.getUser ( )
    hud.stopAllActions ( hUser )
    
	for i = 0, hashtable.getSize ( this.htInterpolators ( ) ) - 1 do
        hud.callAction ( hUser, "action" .. i, this.nHudTime ( ) )
    end
	
--------------------------------------------------------------------------------
end
