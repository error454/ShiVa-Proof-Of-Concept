function CameraFX.onSetCamera ( hCamera, nSpeed )
--------------------------------------------------------------------------------
	
    if(not nSpeed) then
        this.nTrackingSpeed ( 2 )
    else
        this.nTrackingSpeed ( nSpeed )
    end
    
	this.hCurrentCamera ( hCamera )
	this.TrackObject ( )
    
--------------------------------------------------------------------------------
end
