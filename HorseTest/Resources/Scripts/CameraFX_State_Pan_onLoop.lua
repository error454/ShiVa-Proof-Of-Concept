function CameraFX.Pan_onLoop ( )
--------------------------------------------------------------------------------
	
    local dt = application.getLastFrameTime ( )
	local x, y, z = table.getRangeAt ( this.tTranslation ( ), 0, 3 )
    object.translate ( application.getCurrentUserActiveCamera ( ), x * dt, y * dt, z * dt, object.kLocalSpace )
	
--------------------------------------------------------------------------------
end
