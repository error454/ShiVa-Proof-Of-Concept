function User.valuesInit ( )
--------------------------------------------------------------------------------
	
    local t = this.tValues ( )
	table.empty ( t )
    
    local table_add = table.add
    
    for i = 0, this.nSegments ( ) - 1 do
        table_add( t, i + 1 )
    end
    
    table.shuffle ( t )
    table.shuffle ( t )
	
--------------------------------------------------------------------------------
end
